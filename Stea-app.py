def ozon_pricing(params):
    采购成本 = params['采购成本']  
    采购成本利润率 = params['采购成本利润率']
    物流费用 = params['物流费用']  
    其他费用 = params['其他费用']
    固定费用 = params['固定费用']
    汇率 = params['汇率']
    促销折扣 = params['促销折扣']
    类目佣金 = params['类目佣金'] 
    汇率损失 = params['汇率损失']
    货物损失 = params['货物损失']
    利润率 = params['利润率']

    # 采购成本加成计算
    成本加成 = 采购成本 * (1 + 采购成本利润率/100)  
    
    # 其他费用计算 
    总成本 = 成本加成 + 物流费用 + 其他费用 + 固定费用   
    
    # 汇率调整
    总成本 = 总成本 * 汇率
    
    # 折扣与损耗计算
    价格 = 总成本 / ((1 - 促销折扣/100) *  
                         (1 - 类目佣金/100) * 
                         (1 - 汇率损失/100) *  
                         (1 - 货物损失/100))
                         
    # 利润计算                
    利润 = 价格 * 利润率/100
    return 价格, 利润  

